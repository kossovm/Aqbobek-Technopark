-- ============================================================================
-- 09_partial_status.sql
--
-- Добавляем значение 'partial' в enum inventory_status.
-- Используется когда часть штук взята, часть на месте.
-- Сама логика пересчёта статуса делается в коде (scanner.ts).
-- ============================================================================

-- ALTER TYPE ... ADD VALUE требует PG 9.6+ и не может выполняться в транзакции,
-- но Supabase SQL Editor запускает каждый стейтмент отдельно — всё ок.
ALTER TYPE inventory_status ADD VALUE IF NOT EXISTS 'partial';
